package ex3.ativ6;

public class Carta {
    private int numero;
    private String naipe;
    
    public Carta(int numero, String naipe) {
        this.numero = numero;
        this.naipe = naipe;
    }
    
    public int getNumero() {
        return this.numero;
    }
    
    public String getNaipe() {
        return this.naipe;
    }
}
